Why aren't you using git-flow?
------------------------------

If you're not doing versioned releases, git-flow might not be a right fit for you.
However, if you work on a project that's semantically versioned, like a Rubygem or a versioned API, 
    git-flow will give you a couple of simple commands that will do a lot of work under the hood, 
    making working on features, pushing new releases and hotfixing bugs a lot easier.

